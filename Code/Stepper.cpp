#include "Stepper.h"
#include <Arduino.h>

Stepper::Stepper(){
   
    pinMode(_STEPPER_MOTOR, OUTPUT);
    pinMode(_DIR_STEPPER_MOTOR, OUTPUT);
}

// Move the stepper motor by one step
void Stepper::move_stepper_motor(){
    
    digitalWrite(_STEPPER_MOTOR, HIGH);
    delayMicroseconds(_DELAY_STEPPER_MOTOR);
    digitalWrite(_STEPPER_MOTOR, LOW);
    delayMicroseconds(_DELAY_STEPPER_MOTOR);
}

// Calibrate the stepper motor
void Stepper::calibrate_stepper_motor(){
    
    while(!is_Ir_triggered()){
        move_stepper_motor();
    }
}
